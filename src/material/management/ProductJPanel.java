    private void butSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butSearchActionPerformed
        // TODO add your handling code here:
       
        String selectedItem = (String) jComboBox1.getSelectedItem();
        try{
            if (selectedItem.equals("IDProduct")) 
            {
                if (txtIDProduct.getText().equals("")) JOptionPane.showMessageDialog(this, "No information to search");
                else {
                try{
                      list = ProductDAO.getInstance().selectByCondition("IDProduct like '%"+txtIDProduct.getText()+"%'");
                      showData();
                 } catch (Exception e)
                    {
                     e.printStackTrace();
                    }
                }
            }
            else if(selectedItem.equals("ProductName"))
            {
                if (txtProductName.getText().equals("")) JOptionPane.showMessageDialog(this, "No information to search");
                else {
                try{
                      list = ProductDAO.getInstance().selectByCondition("ProductName like'%"+txtProductName.getText()+"%'");
                      showData();
                 } catch (Exception e)
                    {
                     e.printStackTrace();
                    }
                }
            }
            else if(selectedItem.equals("Category"))
            {
                String selectedItem1 = (String) jComboBoxCategory.getSelectedItem();
                if (selectedItem1.equals("")) JOptionPane.showMessageDialog(this, "No information to search");
                else {
                try{
                      list = ProductDAO.getInstance().selectByCondition("TypeName like '%"+selectedItem1+"%'");
                      showData();
                 } catch (Exception e)
                    {
                     e.printStackTrace();
                    }
                }
            }
            else if(selectedItem.equals("Amount"))
            {
                if (txtAmount.getText().equals("")) JOptionPane.showMessageDialog(this, "No information to search");
                else {
                try{
                      list = ProductDAO.getInstance().selectByCondition("Amount like '%"+Integer.parseInt(txtAmount.getText())+"%'");
                      showData();
                 } catch (Exception e)
                    {
                     e.printStackTrace();
                    }
                }
            }
            else if(selectedItem.equals("PriceBuy"))
            {
                if (txtPriceBuy.getText().equals("")) JOptionPane.showMessageDialog(this, "No information to search");
                else {
                try{
                      list = ProductDAO.getInstance().selectByCondition("PriceBuy like '%"+Integer.parseInt(txtPriceBuy.getText())+"%'");
                      showData();
                 } catch (Exception e)
                    {
                     e.printStackTrace();
                    }
                }
            }
            else if(selectedItem.equals("PriceSell"))
            {
                if (txtPriceBuy.getText().equals("")) JOptionPane.showMessageDialog(this, "No information to search");
                else {
                try{
                      list = ProductDAO.getInstance().selectByCondition("PriceSell like '%"+Integer.parseInt(txtPriceSell.getText())+"%'");
                      showData();
                 } catch (Exception e)
                    {
                     e.printStackTrace();
                    }
                }
            }
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_butSearchActionPerformed

