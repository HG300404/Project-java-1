    private void jTableProductMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableProductMouseClicked
        // TODO add your handling code here:
        int index = jTableProduct.getSelectedRow();
        model = (DefaultTableModel) jTableProduct.getModel();

        String IDProduct = model.getValueAt(index, 0).toString();
        String ProductName = model.getValueAt(index, 2).toString();
        String TypeName = model.getValueAt(index, 1).toString();
        String Amount = model.getValueAt(index, 3).toString();
        String PriceBuy = model.getValueAt(index, 4).toString();
        String PriceSell = model.getValueAt(index,5).toString();

        txtIDProduct.setText(IDProduct);
        txtProductName.setText(ProductName);
        jComboBoxCategory.setSelectedItem(TypeName);
        txtAmount.setText(Amount);
        txtPriceBuy.setText(PriceBuy);
        txtPriceSell.setText(PriceSell);
    }//GEN-LAST:event_jTableProductMouseClicked

    private void butDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butDeleteActionPerformed
        // TODO add your handling code here:
        int removeIndex = jTableProduct.getSelectedRow();
        if (removeIndex == -1)
        {
            JOptionPane.showMessageDialog(this,"Select a row to delete!");
        } else  if (list.isEmpty())
        {
            JOptionPane.showMessageDialog(this,"No information to delete!");}
        else
        {
            list.remove(removeIndex);
            showData();
        }

        Product p = new Product();
        p.setIDProduct(Integer.parseInt(txtIDProduct.getText()));
        p.setProductName(txtProductName.getText());
        p.setAmount(Integer.parseInt(txtAmount.getText()));
        p.setPriceBuy(Integer.parseInt(txtPriceBuy.getText()));
        p.setPriceSell(Integer.parseInt(txtPriceSell.getText()));
        
        String selectedItem1 = (String) jComboBoxCategory.getSelectedItem();
        p.setTypeName(selectedItem1);

        ProductDAO.getInstance().delete(p);
        list.remove(p);

        txtIDProduct.setText(ProductDAO.getID());
        txtProductName.setText("");
        txtAmount.setText("");
        txtPriceBuy.setText("");
        txtPriceSell.setText("");
        JOptionPane.showMessageDialog(this,"Delete successful");
    }//GEN-LAST:event_butDeleteActionPerformed

